var kt = generateKotlin();
var dart = generateDart();
var swift = generateSwift();
fs.writeFileSync(path.join(ktDir, "dump_emit.kt"), kt);
fs.writeFileSync(path.join(dartDir, "dump_emit.dart"), dart);
fs.writeFileSync(path.join(swiftDir, "dump_emit.swift"), swift);
